using System.ComponentModel;

namespace Blazix.Slider;

/// <summary>
/// Represents the direction from which the slider appears.
/// </summary>
internal enum SliderDirection
{
    /// <summary>
    /// The slider appears from the left side.
    /// </summary>
    FromLeft,
    /// <summary>
    /// The slider appears from the right side.
    /// </summary>
    FromRight,
    /// <summary>
    /// The slider appears from the top side.
    /// </summary>
    FromTop,
    /// <summary>
    /// The slider appears from the bottom side.
    /// </summary>
    FromBottom
}
